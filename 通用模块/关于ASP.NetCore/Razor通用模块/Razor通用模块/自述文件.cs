
/*开发目标：
  本模块定义了有关Razor最核心，最基本的功能，
  所有使用Asp.NetCore，而且需要展现视图和UI的项目都应当引用本模块

  目标平台：
  本模块被设计为可移植，不应调用任何特定平台的API

  如何使用本模块：
  
  1.将以下代码添加到根页面：
  
  <link href="_content/ToolRazor/css/style.css" rel="stylesheet" />
  <script src="_content/ToolRazor/js/ToolRazor.js"></script>

 2.如果你需要使用Video.js相关的类型，
 还需要将下面的代码添加到根页面：
 
 <link href="https://vjs.zencdn.net/8.5.2/video-js.css" rel="stylesheet" />
 <script src="https://vjs.zencdn.net/8.5.2/video.min.js"></script>

 */