namespace System.Media.Drawing;

/// <summary>
/// 这个枚举表示颜色的色系
/// </summary>
public enum ColourSystem
{
    /// <summary>
    /// 无色系
    /// </summary>
    None,
    /// <summary>
    /// 表示红色占主导
    /// </summary>
    R,
    /// <summary>
    /// 表示绿色占主导
    /// </summary>
    G,
    /// <summary>
    /// 表示蓝色占主导
    /// </summary>
    B
}
