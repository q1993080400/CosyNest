namespace System.Media;

/// <summary>
/// 这个枚举表示媒体文件的类型
/// </summary>
public enum MediumFileType
{
    /// <summary>
    /// 表示该文件不是一个媒体文件
    /// </summary>
    NotMediumFile,
    /// <summary>
    /// 图片文件
    /// </summary>
    Image,
    /// <summary>
    /// 视频文件
    /// </summary>
    Video,
    /// <summary>
    /// 音频文件
    /// </summary>
    Audio
}
