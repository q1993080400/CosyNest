namespace System.DataFrancis;

/// <summary>
/// 这个枚举表示用来筛选数据的逻辑运算符
/// </summary>
public enum LogicalOperator
{
    /// <summary>
    /// 等于
    /// </summary>
    Equal,
    /// <summary>
    /// 不等于
    /// </summary>
    NotEqual,
    /// <summary>
    /// 大于
    /// </summary>
    GreaterThan,
    /// <summary>
    /// 大于等于
    /// </summary>
    GreaterThanOrEqual,
    /// <summary>
    /// 小于
    /// </summary>
    LessThan,
    /// <summary>
    /// 小于等于
    /// </summary>
    LessThanOrEqual,
    /// <summary>
    /// 包含
    /// </summary>
    Contain
}
